// Output_Base.java
// 概要
// メソッドを使った標準出力のサンプルです。
//
// メインクラス
class Output_Base {
    // メインメソッド
    public static void main(String[] args)  {
        // メソッド呼び出し
        Print_Method () ;
    }
    // メソッド:Print_Method
    public static void Print_Method ()
    {
        // 処理：文字の出力
        System.out.println("HelloJAVA");
    }
}
