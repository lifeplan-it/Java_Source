// HelloJava_Method.java
// 概要
// メソッドを使ったHelloJavaです。
//
// メインクラス
class HelloJava_Method {
    // メインメソッド
    public static void main(String[] args)
    {
        // メソッド呼び出し
        HelloJava_Print () ;
    }
    // メソッド:HelloJava_Method
    public static void HelloJava_Print ()
    {
        // 処理：文字の出力
        System.out.println("HelloJAVA");
    }
}
